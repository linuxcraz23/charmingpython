# Handy function while studying loops
# It generates lists containing arithmetic progressions

# simple range example
print("range(10) : ", list(range(10)))

# specifying start and end
print("range(0,10) : ", list(range(0,10)))
print("range(5,10) : ", list(range(5,10)))

# specifying "step" value
print("range(0,20,2) : ", list(range(0,20,2)))
print("This is introduction to range")
